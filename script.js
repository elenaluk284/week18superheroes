  function createHeroCard(hero) {
    const card = document.createElement('div');
    card.classList.add('hero-card');
    card.innerHTML = `
      <img src="${hero.url}" alt="${hero.name}">
      <div class="hero-info">
        <h2>${hero.name}</h2>
        <p><strong>Университет:</strong> ${hero.universe}</p>
        <p><strong>Альтер эго:</strong> ${hero.alterego}</p>
        <p><strong>Занятие:</strong> ${hero.occupation}</p>
        <p><strong>Друзья:</strong> ${hero.friends}</p>
        <p><strong>Суперсилы:</strong> ${hero.superpowers}</p>
        <p>${hero.info}</p>
        <div class="rating">
          <label for="${hero.name}-rating">Оценка:</label>
          <input type="range" id="${hero.name}-rating" min="1" max="5" value="3">
        </div>
      </div>
    `;
    return card;
  }
  
  function initHeroGallery() {
    const heroesContainer = document.getElementById('heroes-container');
    heroes.forEach(hero => {
      const card = createHeroCard(hero);
      heroesContainer.appendChild(card);
    });
  }
  
  initHeroGallery();